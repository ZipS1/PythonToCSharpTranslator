# 9.	В городском зоопарке содержатся животные N (1 ≤ N ≤ 1000) разных видов. Для участия в международной выставке зоопарк должен представить трех животных различных видов. Требуется написать программу, которая вычислит число способов выбрать трех животных для участия в выставке. Например, если в зоопарке два медведя, тигр, лев и пингвин, то есть семь способов выбрать трех животных:
# 1. первый медведь, тигр и лев;
# 2. первый медведь, тигр и пингвин;
# 3. первый медведь, лев и пингвин;
# 4. второй медведь, тигр и лев;
# 5. второй медведь, тигр и пингвин;
# 6. второй медведь, лев и пингвин;
# 7. тигр, лев и пингвин.

import random

# Количество видов
N = 5

# Случайно определяем количество животных каждого вида (от 1 до 10)
counts = []
i = 0
while i < N:
    counts.append(random.randint(1, 10))
    i += 1

sum1 = 0
sum2 = 0
sum3 = 0

idx = 0
while idx < len(counts):
    x = counts[idx]
    sum3 = sum3 + sum2 * x
    sum2 = sum2 + sum1 * x
    sum1 = sum1 + x
    idx += 1

print("Виды (количества по видам):", counts)
print("Число способов выбрать трех животных разных видов:", sum3)
